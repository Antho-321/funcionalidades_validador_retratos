from typing import List

def symmetric_difference(list_str1: str, list_str2: str) -> List[str]:
    """
    Take two newline-separated lists given as raw strings and
    return their symmetric difference (elements present in exactly
    one list, not in both).

    Parameters
    ----------
    list_str1 : str
        First list, e.g. \"\"\"A\nB\nC\nD\"\"\".
    list_str2 : str
        Second list, same format.

    Returns
    -------
    List[str]
        Elements that appear in one list but not the other,
        returned as a sorted list for determinism.
    """
    # Convert each string to a set of trimmed, non-empty lines
    set1 = {line.strip() for line in list_str1.splitlines() if line.strip()}
    set2 = {line.strip() for line in list_str2.splitlines() if line.strip()}

    # Symmetric difference (set XOR) and sort for stable order
    return sorted(set1 ^ set2)


